package Capitulo3_Operadores.Tutorial9_OperadoresIgualdadRelacionales;

public class T9_operadoresIgualdad {
    public static void main(String[] args){
        int valor1=1;
        int valor2=2;

        if (valor1==valor2){
            System.out.println("Valor1==Valor2");
        }
        if (valor1 != valor2){
            System.out.println("Valor1!=Valor2");
        }
        if (valor1 > valor2){
            System.out.println("Valor1>Valor2");
        }
        if (valor1 < valor2){
            System.out.println("Valor1<Valor2");
        }
        if (valor1 <= valor2){
            System.out.println("Valor1<= Valor2");
        }

    }
}
