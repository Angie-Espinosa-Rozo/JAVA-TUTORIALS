package com.twc.Java.Capitulo3.ArithmeticOperators;

public class UnaryOperators {
    public static void main(String[] args) {
        int resultado = +1;
        System.out.println(resultado);
        resultado--;
        System.out.println(resultado);
        resultado++;
        System.out.println(resultado);
        resultado = -resultado;
        System.out.println(resultado);

        boolean success = false;
        System.out.println(success);
        System.out.println(!success);
    }
}
